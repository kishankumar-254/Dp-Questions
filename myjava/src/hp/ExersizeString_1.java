package hp;

public class ExersizeString_1 {
    public static void main(String[] args) {
        String s="JAVA PROGRAM";
        System.out.println("Original String is:" +s);
        int a=s.charAt(0);
        int b=s.charAt(10);
        System.out.println("The zero position is:" +(char)a);
        System.out.println("The 10th position is:" +(char)b);
    }
}
